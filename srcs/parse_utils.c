#include "lem_in.h"

int				is_num(char *str)
{
	char		*tmp;

	tmp = str;
	if (*tmp == '+' || *tmp == '-')
		tmp++;
	while (*tmp)
	{
		if (!ft_isdigit(*tmp))
			return (0);
		tmp++;
	}
	return (1);
}
